class Solution {
public:
    bool haveConflict(vector<string>& a, vector<string>& b) {
        int s1 = ((a[0][0] - '0') * 10 + (a[0][1] - '0')) * 60 + (a[0][3] - '0') * 10 + (a[0][4] - '0');
        int e1 = ((a[1][0] - '0') * 10 + (a[1][1] - '0')) * 60 + (a[1][3] - '0') * 10 + (a[1][4] - '0');
        int s2 = ((b[0][0] - '0') * 10 + (b[0][1] - '0')) * 60 + (b[0][3] - '0') * 10 + (b[0][4] - '0');
        int e2 = ((b[1][0] - '0') * 10 + (b[1][1] - '0')) * 60 + (b[1][3] - '0') * 10 + (b[1][4] - '0');
        if (s1 > s2) {
            swap(s1, s2);
            swap(e1, e2);
        }

        return s2 <= e1;
    }
};